#compression operators >, <, >=, <=, ==,!=
score = 50
high_score = 80
print(score > high_score )
print(score < high_score )
print(score >= high_score)
print(score <= high_score)
print(score == high_score)
print(score != high_score)

#logical operators (and,or,not)
print(score < high_score) and print(score <= high_score)
print(score > high_score) and print(score < high_score)

print(score == high_score) or (score != high_score)
print(score == high_score) or (score > high_score)
